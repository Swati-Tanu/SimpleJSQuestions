//Switch Case
let choice = 4;
switch (choice) {
  case 1: console.log("Hindi");
    break;
  case 2: console.log("English");
    break;
  case 3: console.log("Telgu");
    break;
  case 4: console.log("Punjabi");
    break;
  case 5: console.log("Marathi");
    break;
  default: console.log("Wrong Input");
}