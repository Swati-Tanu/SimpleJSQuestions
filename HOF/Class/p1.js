//A variable can store a function.

let check = function (num){  //It will not include the name anymore.
  if(num%3==0){
    return true;
  } return false;
}

console.log(check(8)); //In order to call the function we will call the variable now.

//Since the function here has no name so it is "Anonymous Function".

//In this case the variable's(check) value can be changed later too.