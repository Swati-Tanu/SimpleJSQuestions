//Comparison operators (i/p:num or string or boolean and o/p: boolean): 1. == (Double equal to, check for only value)

let a = 10;
let b = 10;
console.log(a==b);

let x = "Aman"
let y = "aman"
console.log(x==y);

//2. != (Not equal to)
console.log(x!=y);

//3. === (Triple equal to, check for value and datatype)
let c = 10;
let d = "10";
console.log(c==d);
console.log(c===d);

// !== 
console.log(c!==d);